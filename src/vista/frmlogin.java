    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        // TODO add your handling code here:
        if(txtusuario.getText().equals("admin") && txtpassword.getText().equals("admin")){
            JOptionPane.showConfirmDialog(null,"usuario administrador");
            frminicio frm = new frminicio();
            frm.toFront();
            frm.setVisible(true);
        }
        if(txtusuario.getText().equals("conta") && txtpassword.getText().equals("conta")){
            JOptionPane.showConfirmDialog(null,"usuario contador");
            frminicio frm = new frminicio();
            frm.toFront();
            frm.setVisible(true);
            frminicio.mnuarchivo.setEnabled(false);
            frminicio.mnuconfiguraciones.setEnabled(false);
        }
        if(txtusuario.getText().equals("usu") && txtpassword.getText().equals("usu")){
            JOptionPane.showConfirmDialog(null,"usuario vendedor");
        }
        else{
            JOptionPane.showConfirmDialog(null,"usuario incorrecto");
        }
    }//GEN-LAST:event_btningresarActionPerformed

